# Some string methods
# Convert string to uppercase
upperCase = 'Hello World'.upper()
print(upperCase)

# Convert string to lowercase
lowerCase = 'Hello World'.lower()
print(lowerCase)

# Replace string with value
strReplace = 'Hello World'.replace('World', 'UK')
print(strReplace)

# Split the string
strSplit = 'Hello World'.split()
print(strSplit)

# Join the string
strJoin = ' '.join(['Hello', 'World'])
print(strJoin)

# Count the string
strCount = 'Hello World'.count("o")
print(strCount)

#  Output whether string is a digit
strIsDigitF = 'Hello World'.isdigit()
print(strIsDigitF)

strIsDigitT = '1984'.isdigit()
print(strIsDigitT)